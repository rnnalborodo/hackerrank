package hackerrank.tutorials.daysofCode.day27Testing;

public class Solution {

  public static void main(String[] args) {
    System.out.println("5");
    System.out.println("4 3");               //YES
    System.out.println("-1 3 0 2");
    System.out.println("5 1");               //NO
    System.out.println("0 -1 2 1 4");
    System.out.println("8 3");               //YES
    System.out.println("-3 0 2 3 4 5 6 7");
    System.out.println("7 5");               //NO
    System.out.println("0 -1 -3 -4 -2 3 4");
    System.out.println("6 4");               //YES
    System.out.println("-1 0 4 2 9 10");
  }
}